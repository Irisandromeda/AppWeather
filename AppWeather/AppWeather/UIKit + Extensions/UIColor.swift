import UIKit

extension UIColor {
    
    static func mainBlue() -> UIColor {
        return #colorLiteral(red: 0.3529411765, green: 0.6392156863, blue: 0.9098039216, alpha: 1)
    }
    
    static func lightBlue() -> UIColor {
        return #colorLiteral(red: 0.4197217226, green: 0.692434907, blue: 0.9540333152, alpha: 1)
    }
    
}
